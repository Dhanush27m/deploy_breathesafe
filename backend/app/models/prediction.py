"""
BreatheSafe — Prediction Model
Stores AQI forecasts generated by Prophet + XGBoost pipeline.
"""

from sqlalchemy import Column, DateTime, Float, ForeignKey, Index, Integer, String
from sqlalchemy.orm import relationship
from sqlalchemy.sql import func

from app.database import Base


class Prediction(Base):
    __tablename__ = "predictions"

    id                  = Column(Integer, primary_key=True, index=True)
    city_id             = Column(Integer, ForeignKey("cities.id",
                                 ondelete="CASCADE"), nullable=False, index=True)

    # When this prediction was made vs what date it predicts
    predicted_for_date  = Column(DateTime, nullable=False, index=True)
    created_at          = Column(DateTime(timezone=True),
                                 server_default=func.now(), index=True)

    # Forecast values
    predicted_india_aqi = Column(Float, nullable=False)
    confidence_lower    = Column(Float, nullable=True)  # Lower bound (80% CI)
    confidence_upper    = Column(Float, nullable=True)  # Upper bound (80% CI)
    predicted_category  = Column(String(30), nullable=True)

    # Horizon: 1, 3, or 7 days ahead
    horizon_days        = Column(Integer, nullable=False, default=1)

    # Model metadata
    model_version       = Column(String(50), nullable=True)  # e.g. "xgb_v1.2"
    mae                 = Column(Float, nullable=True)        # Model error at training
    rmse                = Column(Float, nullable=True)

    # Relationship
    city = relationship("City", back_populates="predictions")

    __table_args__ = (
        Index("ix_predictions_city_date", "city_id", "predicted_for_date"),
    )

    def __repr__(self):
        return (f"<Prediction city={self.city_id} "
                f"for={self.predicted_for_date} aqi={self.predicted_india_aqi}>")
